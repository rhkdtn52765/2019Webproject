package com.jica.springshoppingmall.beans;

public class AnswerBean {
	private int answer_idx;
	private int answer_status;
	private String answer_text;
	private int answer_content_idx;
	
	public int getAnswer_idx() {
		return answer_idx;
	}
	public void setAnswer_idx(int answer_idx) {
		this.answer_idx = answer_idx;
	}
	public int getAnswer_status() {
		return answer_status;
	}
	public void setAnswer_status(int answer_status) {
		this.answer_status = answer_status;
	}
	public String getAnswer_text() {
		return answer_text;
	}
	public void setAnswer_text(String answer_text) {
		this.answer_text = answer_text;
	}
	public int getAnswer_content_idx() {
		return answer_content_idx;
	}
	public void setAnswer_content_idx(int answer_content_idx) {
		this.answer_content_idx = answer_content_idx;
	}
	
	
}
